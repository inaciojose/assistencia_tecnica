    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Cliente c = new Cliente();

        c.setNome(jTextField1.getText());
        c.setRG(jTextField2.getText());
        c.setRua(jTextField6.getText());
        c.setBairro(jTextField3.getText());
        c.setCpf(jFormattedTextField3.getText());
        c.setCasa(jNCasa.getText());

        try {
            new ClienteDAO().cadastrarCliente(c);
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jComboClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboClienteActionPerformed
        if(jComboCliente.getSelectedIndex() != 0) {
            Cliente c = listaDeNomes.get(jComboCliente.getSelectedIndex() - 1);
            jTextField1.setText(String.valueOf(c.getCodigo()));
            jFormattedTextField1.setText(c.getCpf());
            jFormattedTextField2.setText(c.getRG());
            jTextField3.setText(c.getRua());
            jTextField4.setText(c.getBairro());
            jTextField5.setText(c.getCasa());
        } else {
            JOptionPane.showMessageDialog(null, "Cliente nÃ£o encontrado");
        }
    }//GEN-LAST:event_jComboClienteActionPerformed

